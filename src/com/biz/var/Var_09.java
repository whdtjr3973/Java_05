package com.biz.var;

public class Var_09 {
	
	public static void main(String[] args) {
		
		char a1 = 'a';
		char a2 = 'b';
		
		// 문자a의 ASCII코드와 문자b의 ASCII코드값을 덧셈 연산
		System.out.println(a1 + a2);
		
		String s1 = "Korea";
		String s2 = "대한민국";
		
		// 문자열의 연결
		System.out.println(s1 + s2);
		
		
	}

}
